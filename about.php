  <center><h2> What is Cyberbullying </h2></center>
<p>:Cyberbullying is the use of technology to harass, threaten, embarrass, or target another person. </p>
<p>:This can happen through social media, text messages, emails, or any other online platforms.It is important to recognize the signs and take action to stop it.</p> 
<p>:The Cyberbullying is bullying that takes place over digital devices like cell phones, computers, and tablets. </p>
	 Cyberbullying can occur through SMS, Text, and apps, or online in social media, forums, or gaming where people can view, participate in, or share content.
  
<p>:Bullying is unwanted, aggressive behavior among school aged children that involves a real or perceived power imbalance.</p> 
     The behavior is repeated, or has the potential to be repeated, over time. 
     Both kids who are bullied and who bully others may have serious, lasting problems.

<p>:Bullying includes actions such as making threats, spreading rumors, attacking someone physically or verbally, and excluding someone from a group on purpose.</p>

<center><h2> What Are the Effects of Cyberbullying?</h2></center>

<p>:Kids have almost constant access to their devices, so cyberbullying is hard to escape. Kids and teens can feel like they never get a break and feel the effects very strongly.</p>

<p>:Kids who are cyberbullied can struggle to concentrate in school, which can affect how well they do there. 
<p>:Cyberbullying that is severe, long-lasting, or happens a lot can cause anxiety, depression, and other stress-related disorders in victims and bullies.</p>
     In rare cases, some kids have attempted or died from suicide.</p>

<p>:Cyberbullies also can be suspended or expelled from school or kicked off of sports teams. Depending on the severity of the cyberbullying, kids also might be in legal trouble.</p>
<center><h2>What Are the Signs of Cyberbullying?</h2></center>
<p>Many kids and teens who are cyberbullied don't want to tell a teacher, parent, or trusted adults, often because they feel ashamed or fear that their devices will be taken away at home.</p>

<h4>Cyberbullying signs to watch for:</h4>

<p>:being emotionally upset during or after using the Internet or the phone</p>
<p>:being very secretive or protective of one's digital life</p>
<p>:spending more time than usual in their room</p>
<p>:withdrawal from or lack of interest in family members, friends, and activities</p>
<p>:avoiding school or group gatherings</p>
<p>:slipping grades and "acting out" in anger at home</p>
<p>:changes in mood, behavior, sleep, or appetite</p>
<p>:suddenly wanting to stop using the computer or device</p>
<p>:being nervous or jumpy when getting a message, text, or email</p>
<p>:avoiding discussions about computer or phone activities</p>
<center><h2>How Can Parents Help?</h2></center>
<p>:-If your child is being cyberbullied,Offer comfort and support. </p>
<p>:-Talking about any bullying experiences you had in your childhood might help your child feel less alone.</p>
<p>:-Let your child know that it's not their fault. Bullying says more about the bully than the victim.
<p>:-Praise your child for doing the right thing by talking to you about it. Remind your child you're in this together.
    Reassure your child that you'll figure out what to do. 
<p>:-Notify the school. Tell the principal, school nurse, or a counselor or teacher about the situation. 
   Many schools, school districts, and after-school clubs have rules for responding to cyberbullying.</p>
<p>:-These vary by district and state. But before reporting the problem, let your child know that you plan to do so, so that you can work out a plan that makes you both feel comfortable.
   Encourage your child not to respond to cyberbullying. Doing so just makes the situation worse.</p>
<p>:-Keep records. Keep screen shots of the threatening messages, pictures, and texts. These can be used as evidence with the bully's parents, school, employer, or even the police.</p>
<p>:-Get help. If your child agrees, meeting with a therapist may help work through feelings. A counselor or mediator at school may work with your child alone or together with the bully.</p>
